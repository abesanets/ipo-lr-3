b = int(input("Введите 2е число: ")) #Запрос данных  у юзера
if a > b: #Блок ветвления
    print("1е число больше 2го") # Вывод в косоль
elif a < b: #Блок ветвления
    print("2е число больше 1го") # Вывод в косоль
else: #Блок ветвления
    print("Числа равны") # Вывод в косоль